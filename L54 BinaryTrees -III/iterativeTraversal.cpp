using namespace std;
class Node{
    public:
        int val;
        Node* left;
        Node* right;
        Node(int val){
            this->val = val;
            this->left = NULL;
            this->right = NULL;
        } 
};

void preorder(Node* root){
    stack<Node* > st;
    vector<int> ans;
    st.push(root);
    while(st.size()>0){
        Node* temp = st.top();
        st.pop();
        ans.push_back(temp->val);
        if(temp->right!=NULL) st.push(temp->right);
        if(temp->left!=NULL) st.push(temp->left);
    }
    for(int i = 0 ; i<7 ; i++){
        cout<<ans[i]<<" ";
    }
}
void postOrder(Node* root){
    stack<Node*> st;
    vector<int> ans;
    st.push(root);
    while(st.size()>0){
        Node* temp = st.top();
        st.pop();
        ans.push_back(temp->val);
        if(temp->left!=NULL) st.push(temp->left);
        if(temp->right!=NULL) st.push(temp->right);
    }
    // reverse(ans.begin(), ans.end());
    for(int i = 6 ; i>=0 ; i--){
        cout<<ans[i]<<" ";
    }
}
void inOrder(Node* root){
    stack<Node*> st;
    Node* node = root;
    while(st.size()>0 || node){
        if(node){
            st.push(node);
            node = node->left;
        }
        else{
            Node* temp = st.top();
            st.pop();
            cout<<temp->val<<" ";
            node = temp->right;
        }
    }
}
